		  	<font face="Arial">Next, measure the length of your saddle &quot;<b>N</b>&quot;.  Now select a head angle "<b>t</b>" in the range of say 72 to 74 degrees 
			(you may wish to consult with your framebuilder!).  Then select a stem length &quot;<b>G</b>&quot; noting that a rough guide is as follows:  for 
			top tube lengths "<b>L</b>" between 45 to 48cm. choose a stem in the range of 8 to 11cm.; for "<b>L</b>" between 50 and 52cm. choose "<b>G</b>" equal to 
			10 to 12cm.; for "<b>L</b>" between 54 and 56cm. choose a stem between 11 and 13cm. and for a 58 to 60cm. top tube choose a stem between 
			12 and 14cm.  Lastly, determine the handlebar clamp diameter "<b>M</b>" that you will be using (generally either 2.58cm. or 3.18cm. for 
			the oversized handlebars).</font><a name="average"></a>
